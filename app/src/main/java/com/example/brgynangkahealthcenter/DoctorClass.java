package com.example.brgynangkahealthcenter;

public class DoctorClass {

    public String  firstname,  lastname, birthday, sex, IDNumber, age, email,  phone, username, password, confirm_password;

    public DoctorClass (String firstname, String lastname, String birthday, String sex, String IDNumber, String age, String email, String phone, String username, String password, String confirm_password) {
        this.firstname = firstname;
        this.lastname = lastname;
        this.birthday = birthday;
        this.sex = sex;
        this.IDNumber = IDNumber;
        this.age = age;
        this.email = email;
        this.phone = phone;
        this.username = username;
        this.password = password;
        this.confirm_password = this.confirm_password;
    }
}
