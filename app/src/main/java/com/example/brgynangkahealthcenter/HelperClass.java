package com.example.brgynangkahealthcenter;

public class HelperClass {

    public String  firstname,  lastname, birthday, sex, familynumber, email,  phone, username, password, confirm_password;

    public HelperClass(String firstname, String lastname, String birthday, String sex, String familynumber, String email, String phone, String username, String password, String confirm_password) {
        this.firstname = firstname;
        this.lastname = lastname;
        this.birthday = birthday;
        this.sex = sex;
        this.familynumber = familynumber;
        this.email = email;
        this.phone = phone;
        this.username = username;
        this.password = password;
        this.confirm_password = this.confirm_password;
    }
}
