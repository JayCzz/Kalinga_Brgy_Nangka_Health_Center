package com.example.brgynangkahealthcenter;

public class NurseClass {

    public String  firstname,  lastname, birthday, sex, IDNumber, email,  phone, username, password, confirm_password;

    public NurseClass (String firstname, String lastname, String birthday, String sex, String IDNumber, String email, String phone, String username, String password, String confirm_password) {
        this.firstname = firstname;
        this.lastname = lastname;
        this.birthday = birthday;
        this.sex = sex;
        this.IDNumber = IDNumber;
        this.email = email;
        this.phone = phone;
        this.username = username;
        this.password = password;
        this.confirm_password = this.confirm_password;
    }
}
